package com.yedam.review;

public class Lotto {
	
	//클래스로 사용할 경우
	private int[] lottoNo;
	private String name;

	public int[] getLottoNo() {
		return lottoNo;
	}

	public void setLottoNo(int[] lottoNo) {
		this.lottoNo = lottoNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
